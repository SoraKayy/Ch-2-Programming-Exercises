#include <iostream>
#include <cmath>

using namespace std;

int main()
{
	cout << "Car gas and distance math \n";
	cout << "------------------------- \n";

	double gas_amt; //current gas amt in the car
	cout << "Enter the number of gas in the tank: ";
	cin >> gas_amt;
	cout << "\n";

	double mpg; //car's mpg
	cout << "Enter MPG: ";
	cin >> mpg;
	cout << "\n";

	double price_per_gallon; //cost of gas
	cout << "What is the price per gallon: $";
	cin >> price_per_gallon;

	cout << "------------------------- \n";
	cout << "\n"; 

	double distance = gas_amt * mpg; //miles left before refill
	cout << "Distance with current gas amount: " << distance << " miles \n";
	cout << "\n";

	double hundred_miles = (100 / mpg) * price_per_gallon;
	cout << "Cost to travel 100 miles in relation to mpg: $" << hundred_miles << "\n";
	cout << "\n";

	return 0;
}

